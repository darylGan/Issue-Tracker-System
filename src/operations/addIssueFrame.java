    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        try{
            int issueID   = 0; // actual issueID will be handled by MySQL auto increment feature
            User assignee = UserQuery.getUser(inputAssigneeID());
            String title = titleField.getText();
            Timestamp time = new Timestamp(new Date(System.currentTimeMillis()).getTime());
            String tag   = tagField.getText();
            int priority = Integer.parseInt(priorityField.getText());
            String description = getText();
            String status = "Open";
            
            Issue newIssue = new Issue( issueID, projectID, currentUser, assignee, title, description, time, tag, priority, status, null);
            IssueQuery.insertNewIssue( newIssue );       
            JOptionPane.showMessageDialog(null, "Added issue succesfully. \nPress Show All to show new issue added.");
            this.dispose();
        }
        
        
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "There is an error with the issue details. \n Please re-enter the proper details.");
        }
    }//GEN-LAST:event_okActionPerformed

