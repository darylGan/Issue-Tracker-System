    public String getText()
    {
        String text = "";
        for(String line: descArea.getText().split("\n")){
            text += line + "\n";
        }
        
        return text;
    }
    
     private int inputAssigneeID() throws SQLException, ClassNotFoundException {
        
         try{
            String nameEntered = assigneeField.getText();

            User[] users = UserQuery.getUsers();
            if(!nameEntered.equals(null)){
                for (User user : users)
                    if (user.getName().equals(nameEntered))
                        return user.getUserID();
            }
         }
         catch (Exception e) {
           JOptionPane.showMessageDialog(null,"Assignee does not exist!");
        }
        return -1;
     }
     
